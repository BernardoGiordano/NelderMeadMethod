function dumpResults(test_params, res)
    formatString = fileread("dumpFormat.txt");
    fd = fopen("dump.txt", 'w+');
    fprintf(fd, formatString, ...
        test_params.dimension, ...
        test_params.sampling_step, ...
        test_params.minimum(1), ...
        test_params.minimum(2), ...
        test_params.minimum(3), ...
        res.minimum(1), ...
        res.minimum(2), ...
        res.minimum(3), ...
        test_params.start_point(1), ...
        test_params.start_point(2), ...
        test_params.start_point(3), ...
        res.f_objective, ...
        test_params.length, ...
        res.length, ...
        test_params.minLength, ...
        res.halvings, ...
        test_params.max_flips, ...
        res.flips, ...
        test_params.tolerance, ...
        res.perc_error ...
    );
    fclose(fd);
end